# Membuat Tupple
nilai = (90,100,88,20,22,20)

# Menggunakan Method Count untuk menghitung total nilai 20
# Mencari ada berapa nilai 20 yang berada pada var nilai
Nilai_20 = nilai.count(20)
print(f"total yang mendapat nilai 20 : {Nilai_20}")

# Mencari indeks dari nilai 90 pada list tuple tersebut yang dimuali dari 0,1,2,dst
Nilai_90 = nilai.index(90)
print(f"nilai 90 berada pada indeks ke-{Nilai_90}")

